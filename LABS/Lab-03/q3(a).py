string = "I Live In Pakistan."
#STRING SIZE
print("Length of String = ", len(string))
#CONCTENATION
word1 = "HELLO, "
word2 = "WORLD"
print("String Concatenation = ", word1 + word2)
#CONCATENATING WHILE ACCESSING ANOTHER DATA TYPE
age = 20
print(f"I am {age} years old.")
#SLICING
print("Slicing = ", string[2:11])
#ACCESSING A CHARACTER
print("Character = ", string[13])
#FINDING INDEX
print("Index of Found Character = ", string.find("I"))
#CONVERT TO UPPERCASE
print("Upper Case = ", string.upper())
#CONVERT TO LOWERCASE
print("Lower Case = ", string.lower())
#CAPITALIZE FIRST LETTER OF THE LINE
string2 = "my name is Abdullah."
print("Capitalization = ", string2.capitalize())